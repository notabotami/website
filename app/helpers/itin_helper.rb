  def self.en_itin_para1
    return "  An Individual Taxpayer Identification Number (ITIN) is a
  tax processing number issued by the Internal Revenue Service.
  It is a nine-digit number that always begins with the number 9 and has a
  range of 70-88 in the fourth and fifth digit. Effective April 12, 2011,
  the range was extended to include 900-70-0000 through 999-88-9999, 900-90-0000
  through 999-92-9999 and 900-94-0000 through 999-99-9999. IRS issues ITINs to
  individuals who are required to have a U.S. taxpayer identification number
  but who do not have, and are not eligible to obtain a Social Security Number (SSN)
  from the Social Security Administration (SSA)."
  end

  def self.es_itin_para1
    return "Un Número de Identificación Personal del Contribuyente (ITIN), es un número tributario procesador otorgado por el Servicio de Impuestos Internos (IRS). Es un número de nueve dígitos que siempre empieza con el número 9 e incluye los números 70-88 como el cuarto y quinto dígito. A partir del 12 de abril de 2011, se ampliaron los números posibles para el cuarto y quinto dígito para incluir del 900-70-0000 al 999-88-9999, 900-90-0000 al 999-92-9999 y 900-94-0000 al 999-99-9999. El IRS otorga los ITIN a individuos quienes requieren un número de identificación para fines tributarios en los Estados Unidos pero no tienen ni son elegibles para obtener un Número de Seguro Social (SSN) de la Administración del Seguro Social (SSA)."
  end

